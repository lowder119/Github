package com.entity;

public class ProgressiveVO {
	private int grade;
	private int lowpower;
	private int highpower;
	private int basic_rate;
	private double additional_rate;
	
	public ProgressiveVO(){}

	public int getGrade() {
		return grade;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}

	public int getLowpower() {
		return lowpower;
	}

	public void setLowpower(int lowpower) {
		this.lowpower = lowpower;
	}

	public int getHighpower() {
		return highpower;
	}

	public void setHighpower(int highpower) {
		this.highpower = highpower;
	}

	public int getBasic_rate() {
		return basic_rate;
	}

	public void setBasic_rate(int basic_rate) {
		this.basic_rate = basic_rate;
	}

	public double getAdditional_rate() {
		return additional_rate;
	}

	public void setAdditional_rate(double additional_rate) {
		this.additional_rate = additional_rate;
	}
	
	
}
